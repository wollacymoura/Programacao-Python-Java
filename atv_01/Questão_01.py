# media aritmética simples:

nota_1 = float(input("digite a primeira nota: "))
nota_2 = float(input("digite a primeira nota: "))
nota_3 = float(input("digite a primeira nota: "))

media_aritmetica = (nota_1 + nota_2 + nota_3)/3
print(f"a média das notas foi igual á: {media_aritmetica}")

# media ponderada considerando os pesos 2, 2 e 3:

nota_1 = float(input("digite a primeira nota: "))
nota_2 = float(input("digite a primeira nota: "))
nota_3 = float(input("digite a primeira nota: "))

media_aritmetica = (nota_1 * 2 + nota_2 * 2 + nota_3 * 3)/7
print(f"a média ponderada com os pesos 2, 2 e 3 foi igual á: {media_aritmetica}")

